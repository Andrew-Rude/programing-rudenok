/**
 * @file main.c
 * @brief Реалізація програми для підрахунку голосних букв у заданому слові чи реченні.
 *
 * @details
 * Програма приймає введений рядок, перевіряє кожен символ і визначає, чи є він голосною буквою.
 * Використовуємо цикл для обробки кожного символа та підрахунку голосних.
 */
 
 #define MAX_LEN 100  // Максимальна довжина рядка
 
/**
 * Головна функція.
 * Алгоритм:
 * - Ввести текст (слово чи речення).
 * - Пройти по кожному символу тексту і перевірити, чи є він голосною.
 * - Підрахувати кількість голосних літер.
 *
 * @return Код успішного завершення програми (0).
 */
 
int main() {
    // Вхідне речення
    char text[MAX_LEN] = "Schwarznegger and Stalone";  // Задаємо тестове речення

    // Лічильник для голосних літер
    int vowel_count = 0;

    // Проходимо по кожному символу в тексті
    for (int i = 0; text[i] != '\0'; i++) { //символ, що позначає кінець рядка — '\0' (нульовий символ)
    
        // Перевіряємо, чи є символ голосною (як малу, так і велику літеру)
        if (text[i] == 'a' || text[i] == 'e' || text[i] == 'i' || text[i] == 'o' || text[i] == 'u' ||
            text[i] == 'A' || text[i] == 'E' || text[i] == 'I' || text[i] == 'O' || text[i] == 'U') {
            vowel_count++;  // Збільшуємо лічильник голосних
        }
    }
    if (vowel_count != 0){} //щоб не видавало помилок
    
    return 0;  // Завершення програми
}
